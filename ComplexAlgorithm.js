// Function to calculate the nth Fibonacci number using memoization
const fibonacci = (() => {
  const memo = {};

  return (n) => {
    if (n <= 0) return 0;
    if (n === 1) return 1;

    if (memo[n]) {
      return memo[n];
    } else {
      memo[n] = fibonacci(n - 1) + fibonacci(n - 2);
      return memo[n];
    }
  };
})();

